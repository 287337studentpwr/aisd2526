void RESET_COMP_ASS(){COMP = 0; ASS = 0;}
void PRINT_COMP_ASS(){std::cout << "\nCOMP = " << COMP << " ASS  = " << ASS << "\n";}

void PRINT_VECTOR(std::vector<int>& A){
    for (int x : A) std::cout << x << " ";
    std::cout <<'\n';
}

void INSERTION_SORT(std::vector<int>& A){
    RESET_COMP_ASS();
    int n = A.size(); ASS++;
    for(int i = 1; i < n; i++){
        int x = A[i]; ASS++;                      
        int j = i - 1; ASS++;
        while(j >= 0){
            COMP++;                 
            if(A[j] > x){
                A[j + 1] = A[j]; ASS++;             
                j = j - 1;
            }
            else break;
        }
        A[j + 1] = x; ASS++;                      
    }
}

void INSERTION_SORT_MOD(std::vector<int>& A){
    RESET_COMP_ASS();

    int n = A.size(); ASS++;   

    for (int i = 1; i < n; i = i + 2){
        int x = A[i - 1]; ASS++;   
        int y = A[i];     ASS++;   

        // sprawdzamy czy x < y
        COMP++;
        if (x > y) std::swap(x, y), ASS+=3;

        int j = i - 2; ASS++;  

        // najpierw wstawiamy y > x
        while (j >= 0){
            COMP++; 
            if (A[j] > y){
                A[j + 2] = A[j]; ASS++;
                j = j - 1; ASS++; 
            } else break;
        }
        A[j + 1] = y; ASS++;

        // teraz x – zaczynamy od j + 1, bo wiemy że y > x
        int k = j + 1; ASS++;  
        while (k >= 0){
            COMP++;
            if (A[k] > x){
                A[k + 1] = A[k]; ASS++;
                k = k - 1; ASS++;
            } else break;
        }
        A[k + 1] = x; ASS++;
    }

    // jezeli n jest nieparzyste to zostaje 1 element nieposortowany na koncu
    if (n % 2 == 1){
        int z = A[n - 1]; ASS++;
        int j = n - 2; ASS++;
        while (j >= 0){
            COMP++;
            if (A[j] > z){
                A[j + 1] = A[j]; ASS++;
                j = j - 1; ASS++;
            } else break;
        }
        A[j + 1] = z; ASS++;
    }
}

